package com.addressBook.model;

public class FacebookAlbumInfoBean {
	private String albumID;
	private String albumName;
	private String albumCoverPhotoID;
	private String albumCoverPhotoURL;
	private String albumPicsCount;
	/**
	 * @return the albumID
	 */
	public String getAlbumID() {
		return albumID;
	}
	/**
	 * @param albumID the albumID to set
	 */
	public void setAlbumID(String albumID) {
		this.albumID = albumID;
	}
	/**
	 * @return the albumName
	 */
	public String getAlbumName() {
		return albumName;
	}
	/**
	 * @param albumName the albumName to set
	 */
	public void setAlbumName(String albumName) {
		this.albumName = albumName;
	}
	/**
	 * @return the albumCoverPhotoID
	 */
	public String getAlbumCoverPhotoID() {
		return albumCoverPhotoID;
	}
	/**
	 * @param albumCoverPhotoID the albumCoverPhotoID to set
	 */
	public void setAlbumCoverPhotoID(String albumCoverPhotoID) {
		this.albumCoverPhotoID = albumCoverPhotoID;
	}
	/**
	 * @return the albumCoverPhotoURL
	 */
	public String getAlbumCoverPhotoURL() {
		return albumCoverPhotoURL;
	}
	/**
	 * @param albumCoverPhotoURL the albumCoverPhotoURL to set
	 */
	public void setAlbumCoverPhotoURL(String albumCoverPhotoURL) {
		this.albumCoverPhotoURL = albumCoverPhotoURL;
	}
	/**
	 * @return the albumPicsCount
	 */
	public String getAlbumPicsCount() {
		return albumPicsCount;
	}
	/**
	 * @param albumPicsCount the albumPicsCount to set
	 */
	public void setAlbumPicsCount(String albumPicsCount) {
		this.albumPicsCount = albumPicsCount;
	}
}
